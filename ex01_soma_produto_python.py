def Soma_Produto():
    numum = int(input("Digite o primeiro número: "))
    numdois = int(input("Digite o segundo número: "))
    soma = numum + numdois
    print(f"Soma: {numum} + {numdois} = {soma}")
    produto = numum * numdois
    print(f"Produto: {numum} * {numdois} = {produto}")
Soma_Produto()
