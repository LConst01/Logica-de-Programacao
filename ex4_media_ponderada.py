nota_um = float(input("Escreva sua nota do primeiro bimestre: "))
nota_dois = float(input("Escreva sua nota do segundo bimestre: "))
nota_tres = float(input("Escreva sua nota do terceiro bimestre: "))
nota_quatro = float(input("Escreva sua nota do quarto bimestre: "))

media = (nota_um  + nota_dois * 2 + nota_tres * 3 + nota_quatro * 4) / 10

print(f"Sua média do é: {media}")